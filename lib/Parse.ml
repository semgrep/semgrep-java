            | Alt (1, v) ->
                `Choice_open (
                  (match v with
                  | Alt (0, v) ->
                      `Open (
                        Run.trans_token (Run.matcher_token v)
                      )
                  | Alt (1, v) ->
                      `Module (
                        Run.trans_token (Run.matcher_token v)
                      )
                  | _ -> assert false
                  )
